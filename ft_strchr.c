#include "libft.h"

char	*strchr(const char *s, int c)
{
	char	ch;

	if (s == 0 || !(0 <= c && c <= 127))
		return (0);
	ch = (char)c;
	while (*s != '\0')
	{
		if (*s == ch)
			return (s);
		s++;
	}
	return (0);
}
