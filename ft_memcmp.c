#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	if (s1 == 0 || s2 == 0 || n == 0)
		return (0);
	while (n && !(*s1 == *s2))
	{
		if (*s1 == '\0' && *s2 == '\0')
			break ;
		n--;
	}
	return (*s1 - *s2);
}
