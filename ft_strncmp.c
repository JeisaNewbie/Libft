#include "libft.h"

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	if (s1 == 0 || s2 == 0 || n == 0)
		return (0);
	while (n && (*s1 == *s2))
	{
		if (*s1 == '\0' && *s2 == '\0')
			break ;
		n--;
	}
	return (*s1 - *s2);
}
