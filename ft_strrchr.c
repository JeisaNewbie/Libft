#include "libft.h"

char	*strrchr(const char *s, int c)
{
	char	ch;
	int		i;

	if (s == 0 || !(0 <= c && c <= 127))
		return (0);
	ch = (char)c;
	i = 0;
	while (s[i])
		i++;
	while (i >= 0)
	{
		if (s[i] == ch)
			return (&s[i]);
		i--;
	}
	return (0);
}
